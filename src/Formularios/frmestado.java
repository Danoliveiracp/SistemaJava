    private void gravarestActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gravarestActionPerformed

        try {

            if (nomest.getText().equals("") || siglaest.getText().equals("")) {

                JOptionPane.showMessageDialog(nomest, "Ah campos obrigatÃ³rios, que nÃ£o foram preenchidos!");
                gravarest.setEnabled(true);
                cancest.setEnabled(true);
                novoest.setEnabled(false);
                alterarest.setEnabled(false);
                excluirest.setEnabled(false);
                nomest.setEnabled(true);
                siglaest.setEnabled(true);
            } else {
                //nomest.setText("");
                //siglaest.setText("");
                nomest.setEnabled(false);
                siglaest.setEnabled(false);
                gravarest.setEnabled(false);
                alterarest.setEnabled(false);
                excluirest.setEnabled(false);
                novoest.setEnabled(true);
                cancest.setEnabled(false);

                PreparedStatement pst = conecta.conn.prepareStatement("insert into estados (nome,sigla_estado) values (?,?)");
                pst.setString(1, nomest.getText());
                pst.setString(2, siglaest.getText());
                pst.execute();
                JOptionPane.showMessageDialog(rootPane, "Cadastro realizado com sucesso!");
                preenchertabela("select * from estados order by id_estado");
            }
        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(rootPane, "Erro ao realizar Cadastro!\n Erro:" + ex);
        }


    }//GEN-LAST:event_gravarestActionPerformed

