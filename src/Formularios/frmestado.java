    public void preenchertabela(String sql) {

        ArrayList dados = new ArrayList();

        String[] colunas = new String[]{"CÃDIGO", "ESTADO", "SIGLA"};

        conecta.executasql(sql);

        try {
            conecta.rs.first();
            do {
                dados.add(new Object[]{conecta.rs.getInt("id_estado"), conecta.rs.getString("nome"), conecta.rs.getString("sigla_estado")});
            } while (conecta.rs.next());
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Preencher Tabela" + ex);
        }

        modelotabela modelo = new modelotabela(dados, colunas);

        tablest.setModel(modelo);

        tablest.getColumnModel().getColumn(0).setPreferredWidth(110);
        tablest.getColumnModel().getColumn(0).setResizable(false);
        tablest.getColumnModel().getColumn(1).setPreferredWidth(380);
        tablest.getColumnModel().getColumn(1).setResizable(false);
        tablest.getColumnModel().getColumn(2).setPreferredWidth(165);
        tablest.getColumnModel().getColumn(2).setResizable(false);
        tablest.getTableHeader().setReorderingAllowed(false);
        tablest.setAutoResizeMode(tablest.AUTO_RESIZE_OFF);
        tablest.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
    }

